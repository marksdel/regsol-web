					<b>About this Event:</b>
					<br><br>
Are you missing out on engaging with potential clients because your website isn’t fully
accessible or your meeting space isn’t physically accessible? Are you fearful of engaging with
clients who have identified vulnerabilities because you don’t know how to navigate those
needs or know what reasonable accommodations should be offered?
The Consumer Protection Code requires regulated entities to have regard to and assist
‘vulnerable customers’. However, access to financial services goes way beyond
‘vulnerability’. Available statistics show that a significant proportion of Irish adults require
assistance in navigating websites and have difficulty finding accessible information on
financial services.
This half day in-person event is designed to inform, encourage and support Financial
Advisors in embracing a hitherto under-served market. You will learn what the legal and
regulatory requirements are but more importantly how best to support individuals that do
require some assistance to ensure your services are truly accessible.
